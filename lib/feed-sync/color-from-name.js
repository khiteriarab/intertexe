const SORTED_COLOR_TERMS = Object.keys(COLOR_NAME_MAP).sort((a, b) => b.length - a.length);

export function extractColorFromName(productName) {
  const nameLower = (productName || '').toLowerCase();
  if (!nameLower) return undefined;
  for (const term of SORTED_COLOR_TERMS) {
    if (nameLower.includes(term)) return COLOR_NAME_MAP[term];
  }
  return undefined;
}
